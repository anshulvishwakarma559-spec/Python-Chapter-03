#Escape sequences  :- Escape sequences let you use special formatting in strings.
# Escape sequences let you format strings in special ways.
'''
   Escape sequence              Description                Example
\n                          New line                    "Hello\nWorld" -> prints on two lines
\t                          Tab space                   "A\tB" -> adds a tab between A and B
\\                          Backslash                   "C:\\new folder" -> C:\ newfolder
\'                          Single quote                'it\'s great' -> it's great 
\"                          Double quote                "He said \"Hi\"" -> He said "Hi"
'''
#Example : 
print("Hello\nWorld")
print("Hello\tWorld")
print("C:\\Users\\Anshul")
print('Hello\'World')
print("He said \"hi\"") 
